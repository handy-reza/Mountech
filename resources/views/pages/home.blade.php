        <h6>By Alfanda - March 14, 2021</h6>
        <p><img src="{{asset('frontend/image/nico360.jpg')}}" alt="Apple Watch" style="width:400px;height:200px;margin :0px 10px 10px 0px;">
            Perusahaan pengembang dan produsen kamera NicoCam berniat untuk merilis kamera yang diberi nama Nico360. Sesuai namanya, Nico360 memiliki kemampuan untuk merekam dengan format hasil 360º. Yang menarik, dengan ukuran bodi kamera 46 mm x 46 mm x 28 mm (p x l x t) membuat Nico360 menjadi kamera 360º dengan ukuran terkecil di dunia!
        <p>
            Kamera Nico360 memakai dua buah sensor dari Sony dengan kekuatan 16 Mega Pixels (MP) dan diklaim mampu membuat gambar beresolusi 25 MP. Untuk video, Nico360 memiliki kemampuan 30 frame per second (fps).
        <p>
            Seperti pada umumnya kamera dengan fitur 360º yang lain, kamera Nico360 dari NicoCam ini juga bisa digunakan untuk konten yang bisa dilihat melalui perangkat VR Stereoscopic Headset.
            Selain itu, kamera Nico360 juga memberikan 4 opsi mode dalam proses rekam video, yakni Flat Mode, VR Mode, Sphere Mode, dan Planet Mode. Hasil rekaman 360º bisa tersambung dengan baik karena sensor kamera dengan kualitas yang tidak bisa disepelekan.
        <p>
            Fitur lainnya adalah kemampuan untuk siarang langsung (live streaming) dengan bantuan internet melalui jalur Bluetooth generasi keempat dan WiFi. Selain video, kamera terkecil ini juga dilengkapi dengan mikropon untuk merekam suara lengkap dengan fitur penyetabil suara sehingga hasil rekaman akan jauh lebih baik lagi.
        <p>
            Untuk kapasitas penyimpanan, kamera 360º Nico360 menggunakan penyimpanan internal sebesar 32 GB. Tidak perlu khawatir jika ukuran tersebut dianggap belum cukup. Sebab, slot untuk ekspansi kapasitas ke kartu memory berjenis microSD juga sudah disematkan.
        <p>
            Berapa harga Nico360 ini? NicoCam berniat melepas kamera canggih dan imut ini di kisaran harga 3-4 juta rupiah. Selain kamera, mereka juga akan memasarkan aksesoris tambahan seperti baterai dengan kapasitas 5000 mAh dan -tentu saja- tongkat selfie khusus untuk kamera terkecil di dunia ini.
